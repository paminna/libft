#include "libft.h"

void ft_putstr_fd(char *s, int fd)
{
	int i;
	int f;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		f = write(fd,&s[i],1);
		i++;
	}
}